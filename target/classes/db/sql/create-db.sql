/* 전국공중화장실 CSV 데이터 등록 및 인덱스 적용 */

CREATE TABLE TB_TOILET
(TYPE       VARCHAR(20)
,NAME       VARCHAR(100)
,ADDR_R     VARCHAR(200)
,UNI_FLG    VARCHAR(1)
,M_BIG      VARCHAR(10)
,M_SML      VARCHAR(10)
,M_D_BIG    VARCHAR(10)
,M_D_SML    VARCHAR(10)
,M_C_BIG    VARCHAR(10)
,M_C_SML    VARCHAR(10)
,W_BIG      VARCHAR(10)
,W_D_BIG    VARCHAR(10)
,W_C_BIG    VARCHAR(10)
,MNG_AGC    VARCHAR(100)
,CONTACT    VARCHAR(20)
,OPEN_TIME  VARCHAR(20)
,SET_YM     VARCHAR(20)
,ADDR_L     VARCHAR(200)
,LONG       DOUBLE
,LAT        DOUBLE
,BASE_DATE  DATE
) AS SELECT * FROM CSVREAD('classpath:toilet.csv');

CREATE INDEX IDXTOILET ON TB_TOILET(LONG, LAT);